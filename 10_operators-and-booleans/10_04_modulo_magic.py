# Use the modulo operator to confirm which of the values
# shown below are divisible by seven.

num_1 = 42
num_2 = 137
num_3 = 455
num_4 = 1997

result_1 = num_1%7
result_2 = num_2%7
result_3 = num_3%7
result_4 = num_4%7


if result_1 == 0:
    print(num_1,"is divisible by seven")

elif result_2 == 0:
    print(num_2,"is divisible by seven")

elif result_3 == 0:
    print(num_3,"is divisible by seven")

elif result_4 == 0:
    print(num_4,"is divisible by seven")


